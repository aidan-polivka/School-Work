void Evaluate::operate(double** ppPop)
{
	// Evaluate the fitness of each individual in the population, by calling
	// the Problem's evaluateChromosome() funciton.
	for (int i = 0; i < popSize; i++)
	{
		pFitnesses[i] = pProblem->evaluateChromosome(ppPop[i]);
	}
}